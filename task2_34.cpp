/*
Даны цифры двух целых чисел: двузначного
2 1 a a
и однозначного b, где
1a —

число единиц,

2 a — число десятков. Получить цифры числа, равного сумме

заданных чисел (известно, что это число двузначное). Слагаемое — двузнач-
ное число и число-результат не определять; условный оператор не использо-
вать.
*/

#include <iostream>
using namespace std;

int main() {
    int a2, a1, b;
    cout << "Введите цифры двузначного числа (a2 — десятки, a1 — единицы): ";
    cin >> a2 >> a1;
    cout << "Введите однозначное число b: ";
    cin >> b;


    int sum = a2 * 10 + a1 + b;

    int c2 = sum / 10;  // десятки
    int c1 = sum % 10;  // единицы

    cout << "Цифры суммы: десятки = " << c2 << ", единицы = " << c1 << endl;

    return 0;
}
